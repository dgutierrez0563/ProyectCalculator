    //Boton de Retroceso
    private void btn_RetrocesoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_RetrocesoActionPerformed
        // TODO add your handling code here:
        String auxiliar=txt_ScreenOut.getText();    //Variable auxiliar para copiar el contenido del TextField de pantalla
            if(auxiliar.length()>0){ //Validar que la cadena auxiliar no este vacia
                  auxiliar=auxiliar.substring(0,auxiliar.length()-1); //Auxiliar sera igual al auxiliar desde el indice 0 hasta el ultimo pero restando un indice
                  if(!auxiliar.equals("")){
                      txt_ScreenOut.setText(auxiliar); //Setear el TextFiel con auxiliar
                  }else{
                      txt_ScreenOut.setText("0"); //Setear el TextFiel con 0
                  }            
            }
    }//GEN-LAST:event_btn_RetrocesoActionPerformed
