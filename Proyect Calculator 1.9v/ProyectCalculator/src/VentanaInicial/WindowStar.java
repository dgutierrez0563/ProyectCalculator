    private void btn_ComaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ComaActionPerformed
        // TODO add your handling code here:
        String auxiliar=txt_ScreenOut.getText();  //Variable que captura el contenido del TextField
        if(auxiliar.length()<=0){   //Se valida
            txt_ScreenOut.setText("0.");    //Setear el TextFiel con el valor del numero y la coma
        }else{ 
            if(!presenciaComa(txt_ScreenOut.getText())){    //Condicion si no hay presencia de la coma en el textFiel
            txt_ScreenOut.setText(txt_ScreenOut.getText()+"."); //Setear el TextFiel con el valor que ya tenia y se concatena la coma
            }
        }
        cola=cola+txt_ScreenOut.getText();
    }//GEN-LAST:event_btn_ComaActionPerformed
