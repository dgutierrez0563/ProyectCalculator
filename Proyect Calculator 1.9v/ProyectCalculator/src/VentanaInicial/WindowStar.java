    /**
     * Metodo para validar si hay punto o no en la operacion
     * @param coma  Recibe el parametro de la cantidad
     * @return resultado del boolean
     */
    public boolean presenciaComa(String coma){  //Se recibe parametro
        estadoComa=false;    //Iniciar su estado en False
        for(int i=0;i<coma.length();i++){   //Recorrer la cantidad de caracteres del parametro
            while(coma.substring(i, i+1).equals(".")){  //Validamos los indices
                estadoComa=true; //setear el resultado
                break;  //Break para detener el proceso de busqueda
            }
        }
        return estadoComa;   //Se retorna el resultado
    }
